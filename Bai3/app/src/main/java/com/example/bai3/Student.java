package com.example.bai3;

public class Student {
    private String name;
    private String mssv;

    public Student(String name, String mssv) {
        this.name = name;
        this.mssv = mssv;
    }

    public String getName() {
        return name;
    }

    public String getMssv() {
        return mssv;
    }
}
